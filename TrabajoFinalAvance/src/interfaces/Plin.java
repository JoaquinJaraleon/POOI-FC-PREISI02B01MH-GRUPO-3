    private void continuar_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_continuar_buttonActionPerformed
        String nombre = nombre_textfield.getText();
        String apellido = apellido_textfield.getText();
        String emailText = email_textfield.getText();

        //EMAIL
        String email = null;
        for (char caracter : emailText.toCharArray()) {
            if (!Character.isDigit(caracter)) {
                JOptionPane.showMessageDialog(null, "El texto ingresado no es un email correcto");
                break; // No es necesario seguir verificando si ya encontramos un car√°cter no num√©rico
            }else {
                email = emailText;
            }

            //Solamente aplica para DNI
            String DNI = null;
            String dniText = dni_textfield.getText();

            for (char character : dniText.toCharArray()) {
                if (!Character.isDigit(caracter)) {
                    JOptionPane.showMessageDialog(null, "El texto ingresado no es un n√∫mero correcto");
                    break; // No es necesario seguir verificando si ya encontramos un car√°cter no num√©rico
                }else {
                    DNI = dniText;
                }
            }
            if (!nombre.isEmpty() && !apellido.isEmpty() && !email.isEmpty() && !dniText.isEmpty()){
                CompraExitosa a = new CompraExitosa();
                a.setVisible(true);
                this.setVisible(false);
            }else if (nombre.isEmpty() || apellido.isEmpty() || email.isEmpty() || dniText.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Se debe completar todos los campos");

            } else {
                JOptionPane.showMessageDialog(null, "Se debe completar todos los campos");
            }
        }
    }//GEN-LAST:event_continuar_buttonActionPerformed

