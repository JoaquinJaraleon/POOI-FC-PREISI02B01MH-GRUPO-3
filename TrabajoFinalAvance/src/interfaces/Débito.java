    private boolean validarNombre() {
        String nombre = titularDebText.getText().trim();
        if (!nombre.matches("^[A-Z][a-zA-Z]*(\\s[A-Z][a-zA-Z]*)*$")) {
            JOptionPane.showMessageDialog(this, "El nombre del titular debe contener solo letras y cada palabra debe empezar con mayúscula.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    
    private boolean validarNumeroTarjeta() {
        String numeroTarjeta = tarjetaDebText.getText().trim();
        if (!numeroTarjeta.matches("\\d{16}")) {
            JOptionPane.showMessageDialog(this, "El número de tarjeta debe contener exactamente 16 dígitos.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

    private boolean validarCVC() {
        String cvc = cvcText.getText().trim();
        if (!cvc.matches("\\d{3}")) {
            JOptionPane.showMessageDialog(this, "El CVC debe contener exactamente 3 dígitos.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

    private boolean validarFecha() {
    String fecha = fechaText.getText().trim();
    if (!fecha.matches("\\d{2}/\\d{2}")) {
        JOptionPane.showMessageDialog(this, "La fecha debe contener el formato MM/YY.", "Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    String[] partesFecha = fecha.split("/");
    try {
        int mes = Integer.parseInt(partesFecha[0]);
        int año = Integer.parseInt(partesFecha[1]);

        if (mes < 1 || mes > 12 || año > 23) {
            JOptionPane.showMessageDialog(this, "Fecha de vencimiento inválida.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    } catch (NumberFormatException | ArrayIndexOutOfBoundsException e) {
        JOptionPane.showMessageDialog(this, "La fecha debe contener el formato MM/YY.", "Error", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    return true;
}    

    
