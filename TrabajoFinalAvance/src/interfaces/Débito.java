    private void addFocusListeners() {
        addFocusListenerToField(titularDebText, "Nombre del titular");
        addFocusListenerToField(tarjetaDebText, "Número de tarjeta");
        addFocusListenerToField(fechaText, "Fecha de vencimiento");
        addFocusListenerToField(cvcText, "CVC / CVV");
    }

    private void addFocusListenerToField(javax.swing.JTextField field, String defaultText) {
        field.addFocusListener(new FocusListener() {
            @Override
            public void focusGained(FocusEvent e) {
                if (field.getText().equals(defaultText) && field.getForeground() == Color.GRAY) {
                    field.setText("");
                    field.setForeground(Color.BLACK);
                }
            }

            @Override
            public void focusLost(FocusEvent e) {
                if (field.getText().isEmpty()) {
                    field.setText(defaultText);
                    field.setForeground(Color.GRAY);
                }
            }
        });
    }

    
